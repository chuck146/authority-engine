/** Get all town names across all serviced counties */
export function getAllServicedTowns(): NjTown[] {
  return NJ_SERVICE_COUNTIES.flatMap((c) => c.towns)
}

/** Find which county a town belongs to */
export function findCountyForTown(townName: string): string | null {
  const lower = townName.toLowerCase()
  for (const county of NJ_SERVICE_COUNTIES) {
    if (county.towns.some((t) => t.name.toLowerCase() === lower)) {
      return county.name
    }
  }
  return null
}

/** Total count of towns across all serviced counties */
export function getTotalTownCount(): number {
  return NJ_SERVICE_COUNTIES.reduce((sum, c) => sum + c.towns.length, 0)
}
