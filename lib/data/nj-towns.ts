/**
 * Static mapping of NJ counties to their municipalities.
 * Used by the /locations hub page and bulk generation script.
 *
 * NJ has 564 municipalities across 21 counties. This file covers
 * the ~16 counties Cleanest Painting actively services (excludes
 * far-south counties). Towns are listed alphabetically per county.
 */

export type NjTown = {
  name: string
  slug: string
}

export type NjCounty = {
  name: string
  slug: string
  towns: NjTown[]
}

function town(name: string): NjTown {
  return {
    name,
    slug: name
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, '-')
      .replace(/(^-|-$)/g, ''),
  }
}

